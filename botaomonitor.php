            <h1> CATEGORIAS DE MONITORES</h1>
            <p>
                Convencional
Assim como o tipo convencional, essa categoria abraça todos os monitores comuns de tela plana. Eles costumam ter resolução Full HD, tela plana e proporção de imagem 16:9, a mesma usada em televisores. <br>

Curvo
Se o seu objetivo é ser mais produtivo, considere usar um monitor curvo. As pontas das telas desses monitores são voltadas para dentro, passando a impressão de que estão te envolvendo. Assim, manter o olhar na tarefa que está executando se torna mais fácil e pode melhorar sua produtividade. <br>

Esse tipo de periférico, inclusive, é excelente para alguns tipos de games, como os melhores jogos de corrida ou simuladores. Um monitor curvo faz com que o usuário se sinta dentro de uma cabine de pilotagem. Por outro lado, filmes e séries podem ter um aspecto estranho por causa da curvatura. <br>
Ultrawide
Os monitores convencionais já possuem a tela plana e horizontal. Só que os modelos ultrawide aumentam ainda mais a diferença de proporção entre o horizontal e vertical. Enquanto um monitor comum costuma ter proporção de 16:9, a proporção desse é de 21:9. <br>

Tanto que um monitor Full HD desse tipo tem resolução de 2560 x 1080 pixels, enquanto modelos convencionais seguem o padrão de 1920 x 1080. Eles também costumam apresentar alguns milímetros de curvatura. <br>

Ou seja, eles são bem compridos, mas isso tem seus benefícios. O primeiro deles é ser tão comprido que parece que está usando dois monitores conectados, ao invés de um. Tanto que o software desses monitores costuma já vir preparado para simular um ambiente assim, te permitindo dividir a tela em duas, sem precisar se preocupar em aprender como usar dois monitores ao mesmo tempo. <br>
