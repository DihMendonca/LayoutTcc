            <h1> TIPOS DE MONITORES</h1>
            <p>
                Convencional
Chamado apenas de "monitor", esses modelos convencionais são usados para tarefas rotineiras, como estudo, trabalho ou para assistir a alguns vídeos ou séries. Eles podem se dividir em diversas categorias, de acordo com o formato. <br>

A tela deles costuma ser melhor que a maioria das telas de notebook, apresentando melhor resolução e maior tamanho. Inclusive, até recomendamos que use o monitor como primeira tela, deixando a do notebook como a segunda. De resto, o monitor convencional é aquele que pode ver em qualquer escritório ou casa. <br>

Gamer
O monitor gamer possui algumas particularidades que o tornam diferente dos convencionais. Embora possam ser das mesmas categorias que vamos mostrar logo adiante, ele se diferencia dos monitores para trabalho por ter foco em performance. <br>

Isso significa que um monitor gamer pode exibir movimentação mais fluída dos personagens em um jogo, além do tempo de resposta entre o comando que deu e o que aparece no monitor ser bem menor do que em aparelhos convencionais. Se o seu objetivo é jogar, o monitor gamer te oferecerá bons gráficos e excelente fluidez. <br>
                </p>
