            <h1> ESPECIFICAÇÕES TÉCNICAS, O QUE CADA UMA SIGNIFICA.</h1>
            <p>
                Produtos eletrônicos vêm acompanhados de algo chamada ficha técnica. É nessa lista que são exibidos os principais valores e especificações que ajudam a guiar sua compra. <br>

O problema é que, por ser uma lista técnica, pode ser que você não saiba nem mesmo o que cada uma dessas especificacões signifiquem. O que é resolução, densidade de pixels ou frequência? Como saber se um monitor é bom sem entender o que esses valores significam? <br>

Para que você consiga se informar melhor e saber o que esperar dos monitores, veja abaixo o que significa cada termo presente em uma ficha técnica de monitor! <br>

Tamanho de tela
Esse é o elemento mais simples de entender justamente por ser medido da mesma maneira que televisores. Ou seja, ver que um monitor tem 32 polegadas simplesmente significa que sua tela é maior que a de um aparelho com 24 polegadas. Mas ser maior não significa que o monitor é melhor, como vamos te mostrar nas outras especificações. <br>
Resolução
Outro detalhe que também é considerado em televisores, a resolução significa o número de pixels presentes horizontalmente e verticalmente em um monitor. É o número da resolução, que define se uma tela é HD, Full HD ou 4K, por exemplo. Vale lembrar que, quanto maior a resolução, melhor a qualidade de imagem. <br>

É importante mencionar também que a placa de vídeo do seu computador, seja ela integrada ou dedicada, deve ser compatível com a resolução. Por exemplo, não adianta comprar um monitor 8K se o máximo que a sua placa de vídeo consegue reproduzir é 4K. <br>

Ao fazer isso, você compra um produto do qual não poderá aproveitar o potencial. Veja abaixo quais números indicam quando um monitor é HD, Full HD, 4K, 5K (também chamado de UHD) e 8K. <br>

HD: 1280 x 720
Full HD: 1920 x 1080
4K: 3840 x 2160
5K/UHD: 5120 x 2880
8K: 7680 x 4320
Você pode ler mais no nosso artigo sobre o que é resolução.

Densidade de pixels
Medido em pixels por polegada, a densidade de pixels é o número de pixels presente em cada polegada de uma tela. Quanto maior o número, mais detalhada é a imagem. Nesse sentido, uma boa densidade de pixels é perfeita para quem trabalha com edição de imagens ou vídeos. <br>

Tipo de painel
Atualmente, os monitores são vendido com um dos três tipos de painel: VA, TN ou IPS. Cada painel interfere de maneira diferente na qualidade de imagem e, principalmente, na fidelidade de cores. <br>

TN

O TN é o modelo mais econômico e antigo de painel, é possível trabalhar e estudar tranquilamente nele. No entanto, trabalhar com imagem e vídeo não é indicado por conta da baixa fidelidade de cores. <br>

Vale notar também que o painel TN costuma falhar em exibir cores caso não esteja olhando diretamente para ele. Em outros ângulos, as cores se perdem facilmente. Telas de notebook ou monitores nesse padrão costumam ser os mais baratos. <br>

VA ou SVA

Dependendo da fabricante, esse tipo de painel é chamado de VA ou SVA. Ela tem uma das melhores variações de contraste das cores, além de melhor visibilidade e fidelidade das cores. <br>

Mesmo observando a tela por ângulos diferentes, a fidelidade de cores é mantida. Ainda assim, esse painel não é indicado para jogos, por conta do alto tempo de resposta, que pode prejudicar jogadores mais competitivos. <br>

IPS, AHVA ou PLS

Assim como o padrão anterior, o nome desse tipo de painel também varia de acordo com a fabricante. O monitor IPS possibilita olhar para a tela de diferentes ângulos sem perder a alta fidelidade das cores. Por outro lado, possui contraste menor que os paineis VA. <br>

Atualmente, é o tipo de painel mais indicado para jogos, já que suporta frequências altas e tempo de resposta bem baixo. Não entendeu o que significa esses termos? Então continue lendo a seguir! <br>

Frequência
Também chamado de "taxa de variação" ou "taxa de atualização", a frequência indica o número de vezes que a tela atualiza por segundo. Um monitor 60Hz, por exemplo, reproduz 60 imagens por segundo. Ou seja, quanto maior esse número, mais vezes a imagem do seu monitor atualiza e mais fluídos são os movimentos. <br>

Em um monitor com frequência mais alta, esse efeito deixa de aparecer, mas sem te deixar sem a sensação de velocidade. É fã de jogos competitivos? Então confira esse monitores 144Hz ou esses de 240Hz para ter uma experiência de jogos muito melhor. <br>

Tempo de resposta
De modo bem simples, o tempo de resposta é a medida na qual o pixel do monitor troca de cor do preto para o branco ou para um tom de cinza. Medido em milissegundos, esse é um detalhe que importa apenas para quem é fã de jogos multiplayer competitivos ou jogos com muita velocidade, como League of Legends, CS:GO ou os games da franquia Forza Horizon. <br>

O efeito prático disso é que a movimentação fica mais fluída e rápida em tudo o que você assiste. Além disso, você evita efeitos como o "motion blur", que é causado quando um vídeo é mais rápido do que a TV é capaz de reproduzir. <br>

Em games de corrida, por exemplo, o motion blur é aquele borrão que fica atrás do seu carro quando ele está muito rápido. Esse efeito é causado justamente por conta da TV ou monitor não conseguir acompanhar a velocidade na mudança de cores. <br>

Conexões
Assim como em notebooks, as conexões são basicamente as portas que um monitor possui. Não há um número específico aqui, mas uma definição de quais encaixes há no equipamento. Por exemplo, um monitor pode ter entradas de vídeo DisplayPort, HDMI e VGA. Alguns modelos também possuem porta para fone de ouvido. <br>

Tudo isso é especificado na área de conexões em uma ficha técnica de monitor. É importante verificar quais portas um equipamento possui para verificar se ele é compatível com seu computador ou notebook. <br>

Saiba se a melhor entrada para você é HDMI, VGA, DisplayPort ou DVI.
Dimensões
Não há muito mistério aqui, já que funciona da mesma maneira que móveis e outros aparelhos eletrônicos. São os valores de largura, altura e profundidade do monitor após montados. <br>

Mas vale mencionar que é essencial que conheça as dimensões de um monitor, mas também a do móvel onde pretende colocá-lo. Afinal de contas, não é nada interessante comprar um monitor para descobrir depois que ele não cabe na sua mesa. <br>

Curvatura (apenas em monitores curvos e ultrawide)
Além das dimensões, os monitores curvos e ultrawide também contam com uma curvatura de tela, que definem o quão fechados eles são. Medido em milímetros, quanto maior a curvatura, mais fechadas são as pontas do monitor. <br>
